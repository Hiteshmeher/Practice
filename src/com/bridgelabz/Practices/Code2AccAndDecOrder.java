public class Code2AccAndDecOrder {
	/**
	 * 
	 * @param args
	 */
	public static void main(String[] args) {
		/**
		 * Printing 1 to 5 in Ascending and descending Order
		 */
		int i, j = 5;
		System.out.println("\n<<<<Result>>>>");
		for (i = 1; i <= 5; i++) {
			System.out.println(i + "\t" + j);
			j--;
		}
	}
}